<?php

/**
 * This class stores an entry
 */
class Entry
{ 
    /**
     * Sets or gets the username
     *
     * @var string The user name
     */
    public $username;
    /**
     * Sets or gets the entry id
     *
     * @var int The entry id
     */
    public $entryId;
    /**
     * Sets or gets the entry post
     *
     * @var string The entry
     */
    public $entry;
    /**
     * Sets or gets the creation time formatted as a string
     *
     * @var string The creation time
     */
    public $creation_time;
    /**
     * Sets or gets the member id
     *
     * @var int The member id
     */
    public $memberId;
}
